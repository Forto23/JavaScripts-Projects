// Addition example
function addExample() {
    document.getElementById("Math").innerHTML = 5 + 3; // 5 + 3 = 8
}

// Subtraction example
function subtractExample() {
    document.getElementById("Math").innerHTML = 10 - 4; // 10 - 4 = 6
}

// Multiplication example
function multiplyExample() {
    document.getElementById("Math").innerHTML = 6 * 2; // 6 * 2 = 12
}

// Modulus example
function modulusExample() {
    document.getElementById("Math").innerHTML = 10 % 3; // remainder of 10 รท 3
}

// Increment example
function incrementExample() {
    let x = 7;
    x++; // add 1
    document.getElementById("Math").innerHTML = x; // 8
}

// Decrement example
function decrementExample() {
    let x = 7;
    x--; // subtract 1
    document.getElementById("Math").innerHTML = x; // 6
}

